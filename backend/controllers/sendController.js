// 토큰, 상품정보 등 백엔드에서 가공된 데이터를 클라이언트로 응답하는 범용 컨트롤러

// ===============================
// [ sendToken 컨트롤러 ]
// - JWT 토큰 발급 성공 시 클라이언트(프론트엔드)로 최종 응답 전달
// - 200 OK 상태와 함께 { message, token } 형태로 반환
// ===============================
exports.sendToken = (req, res) => {
  res.status(200).json({
    message: '로그인 성공',
    token: req.token, // 이전 미들웨어에서 생성된 JWT 토큰
  });
};
